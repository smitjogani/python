# Adding and subtracting Matrices

# Adding

import numpy as np 

a = np.array([[1,2],[3,4]])
b = np.array([[4,5],[6,7]])

print("Printing element of first matrix ")
print(a)

print("Printing element of second matrix ")
print(b)

print("Addition of two matrix ")
print(np.add(a,b))

#Subtracting

c = np.array([[1,2],[3,4]])
d = np.array([[4,5],[6,7]])

print("Subtraction eleement of matrix ")
print(np.subtract(c,d))